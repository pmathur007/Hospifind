function durationToMinutes(string) {
    var ans = 0;
    var splitString = string.split(" ");
    if (splitString.length > 2)
    {
        ans += 60 * parseInt(splitString[0]);
        ans += parseInt(splitString[2]);
    }
    else
    {
        ans += parseInt(splitString[0]);
    }
    return ans;
}

function minutesToString(minutes) {
    if (minutes >= 60)
    {
        return (minutes / 60) + ' hr' + (minutes % 60 === 0 ? '' : ' ' + (minutes % 60) + ' min');
    }
    else
    {
        return (minutes % 60) + ' min';
    }
}

